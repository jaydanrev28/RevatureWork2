import java.util.HashSet;

public class HashSet_1 {
    public static void main(String[] args) {
        HashSet<String> countries = new HashSet<String>();
        countries.add("Italy");
        countries.add("Japan");
        countries.add("India");
        countries.add("Morocco");
        countries.add("UAE");
        System.out.println(countries);
    }
}
